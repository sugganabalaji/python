#Proghram for Demonstrating the Functionality of Relational Operators
#RelOpEx.py
a,b=float(input("Enter Val of a:")),float(input("Enter Val of b:"))
print("*"*50)
print("\tResult of Relational Expression")
print("*"*50)
print("\t\t{} > {}={}".format(a,b,a>b))
print("\t\t{} < {}={}".format(a,b,a<b))
print("\t\t{}=={}={}".format(a,b,a==b))
print("\t\t{}!={}={}".format(a,b,a!=b))
print("\t\t{}>={}={}".format(a,b,a>=b))
print("\t\t{}<={}={}".format(a,b,a<=b))
print("*"*50)